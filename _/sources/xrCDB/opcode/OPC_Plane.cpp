Plane& Plane::Set(const icePoint& p0, const icePoint& p1, const icePoint& p2)
{
	icePoint v0 = p1 - p0;
	icePoint v1 = p2 - p0;

	n = v0 ^ v1;
	n.Normalize();

	d = -(p0 | n);

	return *this;
}
